public class StarPattern
{
   public static void main(String[] args)
   {
      // Display the pattern.
		System.out.println("   *");
		System.out.println("  ***");        		// INSERT THREE MISSING STATEMENTS 
      System.out.println(" *****");               //TO COMPLETE THE STAR PATTERN
      System.out.println("*******");
      System.out.println(" *****");
		System.out.println("  ***");
		System.out.println("   *");
   }
}